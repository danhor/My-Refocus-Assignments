//Running for 30 minutes burns 225 calories
//Sam decides to run for 30 minutes in a 15 days.

//Store in a variable named caloriesPerHour the number of calories burns per 30 minutes.
let caloriesPerHour = 225; 

//Count the number of times Sam will run a 15 days and store in a variable named numTimesSamRuns.
let numTimesSamRuns = 15; 

//Store in a variable named runHoursPerDay the number of hours Sam has decided to run.
let runHoursPerDay = 0.5; 

//Compute then total calories burned using caloriesPerHour and numTimesSamRuns
let totalCaloriesBurned = caloriesPerHour * numTimesSamRuns;

//Display then message using console.log.
console.log(`"Great work, Sam! after ${runHoursPerDay} hour/s of running every day for a week, you may lose a total of ${totalCaloriesBurned} calories."`);


